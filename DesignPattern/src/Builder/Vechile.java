package Builder;

/**
 * 定义车辆本身
 * @author Administrator
 *
 */
public class Vechile {
	
	private String body;//车身
	
	private String wheels;//车轮
	
	private String lacquer;//漆面
	
	private String decorate;//装潢
	
	private String eqp;//车内电子设备
	
	private String adjust;//车辆性能调校状况
	
	private String testResult;//出厂检测结果

	public String getBody() {
		return body;
	}

	public void setBody(String body) {
		this.body = body;
	}

	public String getWheels() {
		return wheels;
	}

	public void setWheels(String wheels) {
		this.wheels = wheels;
	}

	public String getLacquer() {
		return lacquer;
	}

	public void setLacquer(String lacquer) {
		this.lacquer = lacquer;
	}

	public String getDecorate() {
		return decorate;
	}

	public void setDecorate(String decorate) {
		this.decorate = decorate;
	}

	public String getEqp() {
		return eqp;
	}

	public void setEqp(String eqp) {
		this.eqp = eqp;
	}

	public String getAdjust() {
		return adjust;
	}

	public void setAdjust(String adjust) {
		this.adjust = adjust;
	}

	public String getTestResult() {
		return testResult;
	}

	public void setTestResult(String testResult) {
		this.testResult = testResult;
	}
	
	
	
}
